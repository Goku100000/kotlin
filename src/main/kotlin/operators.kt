fun main(){
    //Aritmetic Operators
    var num1=60
    var num2=20
    println(num1+num2)
    println(num1-num2)
    println(num1*num2)
    println(num1/num2)
    println(num1%num2)

    //Assignment Operators
    var x=6
    x+=5
    println(x)

    var y=10
    y*=3
    println(y)

    var n=15
    n-=7
    println(n)

    var m=9
    m/=3
    println(m)

    var t=68
    t%=5
    println(t)

    //Comparison operators
    var b=20
    println(b==2)
    println(b<2)
    println(b>2)
    println(b>=2)
    println(b<=2)

    //Logical Operators
    var mylogic=80
    println(mylogic<50 && mylogic==80) //And
    println(mylogic<50 || mylogic==80) //Or
    println(!(mylogic<50 || mylogic==80)) //Not




}